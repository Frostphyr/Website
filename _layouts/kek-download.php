<h2>Installation</h2>
<p>
  KIN Event Keyer does not require any installation but is targeted to run on Java SE 6 which can be downloaded 
  <a href="https://www.oracle.com/technetwork/java/javase/downloads/java-archive-downloads-javase6-419409.html">here</a>.
</p>
<p><strong>NOTE: </strong>Standard Kmart systems should already have Java SE 6 installed</p>